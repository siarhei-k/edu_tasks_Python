# –†–µ—à–µ–Ω–∏–µ –∑–∞–¥–∞—á–∏ –Ω–µ –ø—Ä–æ—à–ª–æ –≤—Å–µ –∞–≤—Ç–æ—Ç–µ—Å—Ç—ã!
z = int(input())
cost = input().split(' ')

price = {'0': 0}
for i in range(1, 10):
    price[str(i)] = int(cost[i-1])

for edge in range(10**5, 0, -1):
    #print('cheked number is ' + str(edge))
    if '0' in list(str(edge)):
        #print('has zero')
        continue
    total = 0
    for el in list(str(edge)):
        total += price[el]
    #print('wage is ' + str(total))
    if total <= z:
        print(edge)
        break
    if edge == 1:
        print(-1)